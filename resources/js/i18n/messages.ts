type MessageValue = string | number | boolean | null | MessageTree;

interface MessageTree {
    [key: string]: MessageValue;
}

function isObject(value: unknown): value is MessageTree {
    return typeof value === 'object' && value !== null && !Array.isArray(value);
}

function deepMerge(base: MessageTree, overrides: MessageTree): MessageTree {
    const merged: MessageTree = { ...base };

    for (const [key, value] of Object.entries(overrides)) {
        const baseValue = merged[key];

        if (isObject(baseValue) && isObject(value)) {
            merged[key] = deepMerge(baseValue, value);
            continue;
        }

        merged[key] = value;
    }

    return merged;
}

